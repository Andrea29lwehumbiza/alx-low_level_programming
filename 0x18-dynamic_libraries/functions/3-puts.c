#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>

/**
 * _puts - Prints a string to the standard output followed by a newline.
 * @s: A pointer to the string to be printed.
 *
 * Description: This function takes a pointer to a string and prints each
 * character of the string to the standard output using the `puts` function.
 * A newline character is appended after the string is printed
 */
void _puts(char *s)
{
	puts(s);
}
